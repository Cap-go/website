const withCollapsed = (value, collapsed) => (collapsed === undefined ? value : { ...value, collapsed })

const linkItem = (label, link) => ({ label, link })

const autogeneratedItem = (label, directory, collapsed) =>
  withCollapsed(
    {
      label,
      autogenerate: { directory },
    },
    collapsed,
  )

const section = (label, items, collapsed) => withCollapsed({ label, items }, collapsed)

const pluginDoc = (label, slug, extraItems = []) =>
  section(label, [linkItem('Overview', `/docs/plugins/${slug}/`), linkItem('Getting started', `/docs/plugins/${slug}/getting-started`), ...extraItems], true)

